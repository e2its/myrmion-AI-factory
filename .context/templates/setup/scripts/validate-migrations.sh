#!/usr/bin/env bash
# ================================================================
# validate-migrations.sh — Migration Safety Validator
# ================================================================
# Scans database migration files for forbidden destructive operations
# as defined in docs/rules/database.md Migration Safety Policy.
#
# Usage:
#   ./scripts/validate-migrations.sh [--strict] [--path <dir>]
#
# Enforcement points:
#   1. IMPLEMENT (🔍 REVIEW hat) — during per-phase review
#   2. QA --verify — as pre-audit blocking check
#   3. CI pipeline — as automated gate
#
# Exit codes:
#   0 = All migrations safe
#   1 = Forbidden destructive operation detected (CRITICAL)
#   2 = Suspicious pattern detected (WARNING in standard, CRITICAL in strict)
#   3 = Configuration error
#
# Generated by Agent: SETUP | Feature: SETUP
# ================================================================

set -euo pipefail

# ── Configuration ────────────────────────────────────────────────
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
STRICT_MODE=false
EXIT_CODE=0

# Migration paths to scan (auto-detected)
MIGRATION_PATHS=()

# ── Argument Parsing ─────────────────────────────────────────────
while [[ $# -gt 0 ]]; do
  case "$1" in
    --strict)   STRICT_MODE=true; shift ;;
    --path)     MIGRATION_PATHS+=("$2"); shift 2 ;;
    --help|-h)
      echo "Usage: $0 [--strict] [--path <dir>]"
      echo ""
      echo "Options:"
      echo "  --strict    Treat warnings as critical errors"
      echo "  --path      Explicit migration directory (can be repeated)"
      echo ""
      echo "Auto-detection:"
      echo "  If --path is not specified, scans common migration directories:"
      echo "    migrations/, db/migrations/, alembic/versions/, prisma/migrations/,"
      echo "    src/migrations/, database/migrations/, flyway/sql/, liquibase/"
      echo ""
      echo "Exit codes:"
      echo "  0 = All safe    1 = Forbidden op    2 = Suspicious    3 = Config error"
      exit 0
      ;;
    *) echo "Unknown option: $1"; exit 3 ;;
  esac
done

# ── Helpers ──────────────────────────────────────────────────────
RED='\033[0;31m'
YELLOW='\033[0;33m'
GREEN='\033[0;32m'
NC='\033[0m'

pass()  { echo -e "${GREEN}✅ PASS${NC}: $1"; }
warn()  { echo -e "${YELLOW}⚠️  WARN${NC}: $1"; }
fail()  { echo -e "${RED}❌ FAIL${NC}: $1"; }

# ── Auto-Detect Migration Paths ─────────────────────────────────
if [[ ${#MIGRATION_PATHS[@]} -eq 0 ]]; then
  CANDIDATE_DIRS=(
    "migrations"
    "db/migrations"
    "alembic/versions"
    "prisma/migrations"
    "src/migrations"
    "database/migrations"
    "flyway/sql"
    "liquibase"
    "src/db/migrations"
    "src/database/migrations"
  )
  
  for dir in "${CANDIDATE_DIRS[@]}"; do
    if [[ -d "${PROJECT_ROOT}/${dir}" ]]; then
      MIGRATION_PATHS+=("${PROJECT_ROOT}/${dir}")
    fi
  done
fi

if [[ ${#MIGRATION_PATHS[@]} -eq 0 ]]; then
  pass "No migration directories found (skipping migration validation)"
  exit 0
fi

echo "═══════════════════════════════════════════════════════════════"
echo "  Migration Safety Validator"
echo "  Scanning: ${MIGRATION_PATHS[*]}"
echo "  Mode: $(if $STRICT_MODE; then echo 'STRICT'; else echo 'STANDARD'; fi)"
echo "═══════════════════════════════════════════════════════════════"
echo ""

# ── Forbidden Operations (ALWAYS CRITICAL) ──────────────────────
echo "── Check 1: Forbidden Destructive Operations ───────────────"
TOTAL_VIOLATIONS=0

# Category A: CRITICAL — Always blocked
declare -A FORBIDDEN_PATTERNS
FORBIDDEN_PATTERNS=(
  ["DROP TABLE"]='DROP\s+TABLE'
  ["DROP DATABASE"]='DROP\s+DATABASE'
  ["DROP SCHEMA"]='DROP\s+SCHEMA'
  ["TRUNCATE TABLE"]='TRUNCATE\s+(TABLE\s+)?'
  ["DELETE without WHERE"]='DELETE\s+FROM\s+\w+\s*;'
  ["DELETE ALL (no WHERE)"]='DELETE\s+FROM\s+\w+\s+WHERE\s+1\s*=\s*1'
  ["DROP COLUMN"]='ALTER\s+TABLE\s+\w+\s+DROP\s+COLUMN'
)

for label in "${!FORBIDDEN_PATTERNS[@]}"; do
  pattern="${FORBIDDEN_PATTERNS[$label]}"
  for migration_dir in "${MIGRATION_PATHS[@]}"; do
    while IFS= read -r match; do
      if [[ -n "$match" ]]; then
        fail "FORBIDDEN: ${label} detected"
        echo "    $match"
        TOTAL_VIOLATIONS=$((TOTAL_VIOLATIONS + 1))
      fi
    done < <(grep -rniE "$pattern" "$migration_dir" \
             --include="*.sql" --include="*.py" --include="*.ts" --include="*.js" \
             --include="*.rb" --include="*.java" --include="*.cs" 2>/dev/null || true)
  done
done

if [[ $TOTAL_VIOLATIONS -gt 0 ]]; then
  fail "${TOTAL_VIOLATIONS} forbidden destructive operation(s) detected!"
  fail "Policy: Destructive operations require ADR exception (docs/rules/database.md)"
  EXIT_CODE=1
else
  pass "No forbidden destructive operations found"
fi
echo ""

# ── Suspicious Patterns (WARNING or CRITICAL in strict) ─────────
echo "── Check 2: Suspicious Patterns ────────────────────────────"
SUSPICIOUS_COUNT=0

declare -A SUSPICIOUS_PATTERNS
SUSPICIOUS_PATTERNS=(
  ["RENAME TABLE"]='RENAME\s+TABLE|ALTER\s+TABLE\s+\w+\s+RENAME\s+TO'
  ["CHANGE column type"]='ALTER\s+TABLE\s+\w+\s+(ALTER|MODIFY)\s+COLUMN\s+\w+\s+(SET\s+DATA\s+)?TYPE'
  ["Raw SQL execute"]='execute\s*\(\s*["\x27]'
  ["Bulk UPDATE without LIMIT"]='UPDATE\s+\w+\s+SET\s+.*WHERE'
)

# Patterns that require PCRE (negative lookahead)
declare -A PCRE_SUSPICIOUS_PATTERNS
PCRE_SUSPICIOUS_PATTERNS=()

# Check if grep -P is available (PCRE support)
if grep -P '' /dev/null 2>/dev/null; then
  PCRE_SUSPICIOUS_PATTERNS=(
    ["Bulk UPDATE without LIMIT (refined)"]='UPDATE\s+\w+\s+SET\s+.*WHERE(?!.*LIMIT)'
  )
fi

for label in "${!SUSPICIOUS_PATTERNS[@]}"; do
  pattern="${SUSPICIOUS_PATTERNS[$label]}"
  for migration_dir in "${MIGRATION_PATHS[@]}"; do
    while IFS= read -r match; do
      if [[ -n "$match" ]]; then
        # For the basic UPDATE pattern, skip if line contains LIMIT
        if [[ "$label" == "Bulk UPDATE without LIMIT" ]] && echo "$match" | grep -qi 'LIMIT'; then
          continue
        fi
        warn "SUSPICIOUS: ${label} detected"
        echo "    $match"
        SUSPICIOUS_COUNT=$((SUSPICIOUS_COUNT + 1))
      fi
    done < <(grep -rniE "$pattern" "$migration_dir" \
             --include="*.sql" --include="*.py" --include="*.ts" --include="*.js" \
             --include="*.rb" --include="*.java" --include="*.cs" 2>/dev/null || true)
  done
done

if [[ $SUSPICIOUS_COUNT -gt 0 ]]; then
  warn "${SUSPICIOUS_COUNT} suspicious pattern(s) found (review recommended)"
  if $STRICT_MODE && [[ $EXIT_CODE -eq 0 ]]; then EXIT_CODE=2; fi
else
  pass "No suspicious patterns found"
fi
echo ""

# ── Check 3: Rollback Scripts Present ───────────────────────────
echo "── Check 3: Rollback/Down Scripts ──────────────────────────"
MISSING_ROLLBACK=0

for migration_dir in "${MIGRATION_PATHS[@]}"; do
  # Check for up/down pattern (common in Flyway, Knex, Sequelize)
  UP_FILES=$(find "$migration_dir" -name "*up*" -o -name "*forward*" 2>/dev/null | wc -l)
  DOWN_FILES=$(find "$migration_dir" -name "*down*" -o -name "*rollback*" -o -name "*revert*" 2>/dev/null | wc -l)
  
  if [[ $UP_FILES -gt 0 && $DOWN_FILES -eq 0 ]]; then
    warn "Migration directory has up-scripts but no down/rollback scripts: $migration_dir"
    MISSING_ROLLBACK=1
  fi
done

if [[ $MISSING_ROLLBACK -eq 1 ]]; then
  warn "Some migrations may lack rollback capability"
  if $STRICT_MODE && [[ $EXIT_CODE -eq 0 ]]; then EXIT_CODE=2; fi
else
  pass "Rollback scripts check OK"
fi
echo ""

# ── Summary ──────────────────────────────────────────────────────
echo "═══════════════════════════════════════════════════════════════"
if [[ $EXIT_CODE -eq 0 ]]; then
  echo -e "  ${GREEN}All migration safety checks passed.${NC}"
else
  echo -e "  ${RED}Migration safety violations detected (exit code: $EXIT_CODE).${NC}"
  echo ""
  echo "  Remediation:"
  echo "  - Forbidden ops: Use safe alternatives (see docs/rules/database.md)"
  echo "  - Exceptions: Require ADR via /BLUEPRINT --adr {FEATURE_ID}"
  echo "  - Suspicious: Review and document justification"
fi
echo "═══════════════════════════════════════════════════════════════"

exit $EXIT_CODE
